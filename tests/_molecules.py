from dataclasses import dataclass
from pyscf import gto


@dataclass(frozen=True)
class MoleculeData:
    atom: str
    spin: int
    charge: int


def get_mole(name: str, **kwargs) -> gto.Mole:
    """Convert to a PySCF gto.Mole."""
    return to_mole(MOLECULES[name], **kwargs)


def to_mole(data: MoleculeData, **kwargs) -> gto.Mole:
    """Convert to a PySCF gto.Mole."""
    mol = gto.M(
        atom=data.atom,
        spin=data.spin,
        charge=data.charge,
        verbose=0,
        **kwargs,
    )
    return mol


